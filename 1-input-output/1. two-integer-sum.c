
// 1. Write a program that takes two integers as input and prints their sum.
// 	Expected Output :
// 	Sample Input: 4 7
// 	Sample Output: 11
#include <stdio.h>
int main(){
    int a,b,c;
    printf("Please Enter any two numbers\n");
    scanf("%d %d",&a,&b);
    c=a+b;
    printf("The sum is : %d",c);    
    return 0;
}
// Output

// Please Enter any two numbers
// 4
// 7
// The sum is : 11
